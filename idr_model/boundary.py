# ─── Граничные условия на поверхности проводящего включения (r = r_inc) ───────

def apply_inclusion_E(v: np.ndarray) -> None:
    """
    |E|² = 0 на поверхности проводника (r = r_inc).

    Электрическое поле не проникает внутрь идеального проводника.
    Граничное условие Дирихле: v[0] = 0.
    """
    v[0] = 0.0


def apply_inclusion_sigma(sigma: np.ndarray) -> None:
    """
    σ = 0 на поверхности проводника (r = r_inc).

    Рекомбинация носителей на проводящей поверхности.
    Граничное условие Дирихле: sigma[0] = 0.
    """
    sigma[0] = 0.0


def apply_inclusion_H(u: np.ndarray, H_inc: float | None = None) -> None:
    """
    Граничное условие для |H|² на поверхности включения (r = r_inc).

    Parameters
    ----------
    u     : массив |H|² (N+1 узлов)
    H_inc : амплитуда H на включении [А/м].
            None → условие Неймана dH/dr=0 (обрабатывается в equations.py).
            Число → Дирихле: u[0] = H_inc².
    """
    if H_inc is not None:
        u[0] = H_inc**2

def apply_wall_H(u: np.ndarray, H_wall: float = H_WALL) -> None:
    """
    |H|² = H_wall² на стенке (r = R).

    Граничное условие Дирихле: u[N] = H_wall².
    """
    u[-1] = H_wall**2


def apply_wall_sigma(sigma: np.ndarray) -> None:
    """
    σ = 0 на стенке (r = R): носители заряда рекомбинируют.

    Граничное условие Дирихле: σ[N] = 0.
    """
    sigma[-1] = 0.0


def apply_all_boundary_conditions(u: np.ndarray,
                                   v: np.ndarray,
                                   sigma: np.ndarray,
                                   H_wall: float = H_WALL) -> None:
    """
    Применяет все граничные условия одновременно.

    Parameters
    ----------
    u     : массив |H|² (N+1 узлов)
    v     : массив |E|² (N+1 узлов)
    sigma : массив σ    (N+1 узлов)
    H_wall: амплитуда H на стенке [А/м]
    """
    apply_axis_E(v)
    apply_wall_H(u, H_wall)
    apply_wall_sigma(sigma)


def get_ghost_node_value(arr: np.ndarray) -> float:
    """
    Возвращает значение фиктивного узла для условия Неймана du/dr=0:
    u[-1] = u[1]  (ghost node слева от i=0).

    Используется в equations.py при сборке уравнения для i=0.

    Parameters
    ----------
    arr : массив (u или sigma), индекс 0 — ось, 1 — первый внутренний узел

    Returns
    -------
    Значение фиктивного узла
    """
    return arr[1]
