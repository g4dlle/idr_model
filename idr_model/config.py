"""
config.py — параметры задачи для 1D модели ИДР (Абдуллин, Желтухин, 1985).

Все величины в системе СИ, если не указано иное.
Давление p хранится в Па; перевод в мм рт. ст. выполняется в physics.py.
"""

import numpy as np

# ── Фундаментальные константы ────────────────────────────────────────────────
E_CHARGE = 1.602176634e-19   # Кл, заряд электрона
M_ELECTRON = 9.1093837015e-31  # кг, масса электрона
EPS_0 = 8.8541878128e-12     # Ф/м, электрическая постоянная

# ── Параметры разряда (значения для воспроизведения рис. 2 статьи) ───────────
# Аргон, p = 133 Па (≈1 мм рт. ст.), R = 0.012 м
P_PA = 133.0          # Па, давление газа
R_TUBE = 0.012        # м, радиус трубки
OMEGA = 2 * np.pi * 1.76e6  # рад/с, частота ВЧ-поля (1.76 МГц)

# Амплитуда магнитного поля на стенке (граничное условие)
#
# Закон Фарадея для цилиндра (равномерное H, аппроксимация):
#   E_wall = ω·μ₀·H_wall·R/2
#   коэффициент = ω·μ₀·R/2 ≈ 2π·1.76e6 · 4π·10⁻⁷ · 0.012/2 ≈ 0.0834 В·м/А
#
# Порог поддержания разряда: νi ≈ Da·λ₁ ≈ 25.7 с⁻¹ достигается при
#   E_eff/p ≈ 2950 В/(м·мм рт. ст.), т.е. E_eff ≈ 2950 В/м при p = 1 торр.
# Минимальный H_wall для поддержания разряда:
#   H_min = E_eff/(0.0834) ≈ 2950/0.0834 ≈ 35 400 А/м.
#
# H_WALL = 100 000 А/м обеспечивает:
#   E_wall ≈ 8340 В/м, E_eff ≈ 5895 В/м, νi_max ≈ 4160 с⁻¹ >> Da·λ₁
# и воспроизводит характерный ИДР-профиль — плоская σ/σ₀ в ядре с резким
# спадом вблизи стенки (ионизация сосредоточена у стенки, диффузия
# заполняет объём).
H_WALL = 100000.0     # А/м

# ── Проводящее включение (цилиндр на оси) ────────────────────────────────────
# r ∈ [R_INCLUSION, R_TUBE] — кольцевая область разряда.
# При R_INCLUSION = 0.0 — стандартная геометрия (без включения).
R_INCLUSION = 0.004   # м, радиус проводящего включения (≈ R/3)
H_INCLUSION = None    # А/м, амплитуда H на включении; None → dH/dr=0 (Неймана)

# ── 2D-геометрия (осесимметричная модель) ─────────────────────────────────
L_TUBE = 0.05         # м, длина трубки по оси z
N_Z = 50              # число интервалов по z
BC_Z_SIGMA = "dirichlet"  # ГУ для σ на торцах: "dirichlet" (σ=0) или "neumann"

# ── Начальная концентрация электронов ────────────────────────────────────────
N_E0 = 1e16           # м⁻³, начальное (затравочное) значение

# ── Параметры аппроксимации для аргона (формулы (20),(21) статьи) ────────────
# pDa = D1 * exp(D2 * (E_eff/p)^2),  [Da] = м²/с,  [p] = мм рт. ст.
D1 = 6.4e-4   # м²·с⁻¹·мм рт. ст.
# D2 из статьи указан для E_eff/p в В/(см·мм рт. ст.); в единицах В/(м·мм рт. ст.)
# D2_SI = D2_paper / 100^2 = 1.94e-3/1e4 = 1.94e-7.
# При типичных полях ICP Da остаётся конечной.
# Для упрощённого запуска используем D2 = 0 (постоянная амбиполярная диффузия).
D2 = 0.0      # (м·мм рт. ст./В)²; 0 — постоянная Da = D1/p

# νi/p = [B1*(E_eff/p)^0.5 + B2*(E_eff/p)^1.5] * exp(-B3*p/E_eff)
#   [νi] = с⁻¹, [p] = мм рт. ст., [E_eff/p] = В/(м·мм рт. ст.)
B1 = 4.65e3   # полная модель (Абдуллин, Желтухин, 1985, аргон)
B2 = 0.0
B3 = 2.63e4   # [E_eff/p] = В/(м·мм рт. ст.)

# ── Частота столкновений электронов (аргон при комнатной температуре) ────────
# νc ≈ 5.6e9 * p [мм рт. ст.] с⁻¹  (линейная по давлению)
NU_C_PER_TORR = 5.6e9   # с⁻¹ / мм рт. ст.

# ── Параметры численного метода ───────────────────────────────────────────────
N_GRID = 100          # число интервалов (узлов: N+1)
MAX_ITER = 500        # максимальное число итераций (power iteration)
TOL = 1e-6            # критерий сходимости max|Δσ/σ|
RELAX = 0.5           # коэффициент под-релаксации (0 < ω ≤ 1)

# Шаг псевдо-времени для метода установления (используется при dt=DT в solve_idr).
# Примечание: IMEX-метод (явная ионизация, неявная диффузия) устойчив только
# при νi < Da·λ₁ (разряд ниже порога). В ИДР-режиме (νi >> Da·λ₁)
# нарастание на шаг ≈ (1 + DT·νi)/(1 + DT·Da·λ₁) > 1 → нужен малый DT.
# При νi_max ≈ 4160 с⁻¹ и DT = 5e-5: нарастание ≈ 1.21 за шаг,
# что ведёт к переполнению за ~5000 итераций.
# Рекомендуется использовать метод мощностных итераций (dt=None, по умолчанию).
DT = 5e-5             # с, шаг псевдо-времени для IMEX-метода (не используется по умолчанию)
MAX_ITER_DT = 5000    # увеличенный лимит для метода установления
